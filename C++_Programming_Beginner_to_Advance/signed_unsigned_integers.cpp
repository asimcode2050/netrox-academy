#include <iostream>
#include <cstdint>
using namespace std;
int main(){
    signed char myChar = 0; //  -128 to +127
    signed short myShort = 0; //  -32768 to +32767  
    signed int myInt = 0; // -2^31 to +2^31-1
    signed long myLong = 0; // -2^31 to +2^31-1

    unsigned char myUChar = 0; // 0 to 255
    unsigned short myUShort = 0; // 0 to 65535
    unsigned int myUInt = 0; // 0 to 2^32-1
    unsigned long myULong = 0; // 0 to 2^32-1
    
    return 0;
} 
