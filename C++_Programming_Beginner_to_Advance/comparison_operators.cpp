#include <iostream>
using namespace std;
int main(){
    bool b = (2 == 3); //false // equal to
    b = (2 != 3); // true // not equal to
    b = (2 > 3); // false // greater than
    b = ( 2 < 3); // true // less than
    b = (2 >= 3); //false // greater than or equal to
    b = (2 <= 3); // true // less than or equal to 
    cout << b << endl;

    return 0;
} 
