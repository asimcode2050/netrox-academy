#include <iostream>
#include <cstdint>
using namespace std;
int main(){
    int x = 3 + 2; // 5 // addition
    x = 3 - 2; // 1 // subtraction
    x = 3 * 2; // 6 // multiplication
    x = 3 / 2; // 1 // division
    float m = 3 / (float)2; // 1.5
    x = 3 % 2; // 1 // modulus (remainder)
   cout << "M: " << m << endl;
   cout << "X: " << x <<endl;
    
    return 0;
} 
